impl std::fmt::Display for Token {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        let rust_token: scarf_parser::Token<'_> = self.into();
        rust_token.fmt(f)
    }
}

